#include "../inc/libft.h"

int	ft_isalnum(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (1);
	if (c < '0' || c > '9')
		return (0);
	return (1);
}
